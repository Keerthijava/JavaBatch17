package com.practice.operators;

public class RelationalOperator {

	public static void main(String[] args) {
		int DadAge = 40;
		int MomAge = 38;
		// System.out.println(DadAge == MomAge);
		/*
		 * == 
		 * != 
		 * > 
		 * < 
		 * >= 
		 * <=
		 */
		if ((DadAge <= MomAge)) {
			System.out.println(true);
		} else {
			System.out.println(false);
		}

	}
}
