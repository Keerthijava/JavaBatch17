package com.practice.controlstatements;

public class IfElseExample {

	public static void main(String[] args) {
	int age1=40;
	int age2=20;
	if(age1>age2)
	{
		System.out.println("age1 is older");
	}
	else if(age1==age2) {
		System.out.println("both are equal");
	}
	else {
		System.out.println("age2 is older");
	}
	}

}
